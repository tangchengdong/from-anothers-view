function detectRoleType(roleName) {
  const name = roleName || ''
  for (const pattern of ROLE_PATTERNS) {
    if (pattern.keywords.length === 0) continue
    for (const keyword of pattern.keywords) {
      if (name.includes(keyword)) {
        return pattern
      }
    }
  }
  return ROLE_PATTERNS.find(p => p.type === 'default')
}

export function generateOpinionTemplates(roleName) {
  const roleType = detectRoleType(roleName)
  return {
    opinion_templates: roleType.templates,
    speaking_style: roleType.speaking_style,
    emoji: roleType.emoji,
    description: roleType.getDescription(roleName)
  }
}

