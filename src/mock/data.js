function getLocalImagePath(localImageName) {
  if (!localImageName) return null
  try {
    return new URL(`../assets/characters/${localImageName}.webp`, import.meta.url).href
  } catch (e) {
    return null
  }
}

function getCardImagePath(cardImageName) {
  if (!cardImageName) return null
  return `${import.meta.env.BASE_URL}cards/${cardImageName}`
}

function matchCategory(newsCategory, interestedCategories) {
  if (!interestedCategories || interestedCategories.length === 0) return 0
  if (newsCategory === 'general') return 0.3
  const newsCat = NEWS_CATEGORIES[newsCategory]
  if (!newsCat) return 0
  for (const cat of interestedCategories) {
    const catKey = Object.keys(NEWS_CATEGORIES).find(k => NEWS_CATEGORIES[k].name.includes(cat) || cat.includes(NEWS_CATEGORIES[k].name))
    if (catKey === newsCategory) return 1
  }
  return 0.1
}

function generateOpinion(role, news) {
  if (!role) {
    return '这件事值得关注。'
  }

  let roleWithTemplates = role
  if (!role.opinion_templates || role.is_custom) {
    const generated = generateOpinionTemplates(role.name)
    roleWithTemplates = {
      ...role,
      opinion_templates: generated.opinion_templates,
      speaking_style: role.speaking_style || generated.speaking_style
    }
  }

  const category = news.category || 'general'
  let templates = roleWithTemplates.opinion_templates[category]
  if (!templates || templates.length === 0) {
    templates = roleWithTemplates.opinion_templates.general || ['这件事值得关注。']
  }
  let opinion = templates[Math.floor(Math.random() * templates.length)]
  opinion = opinion.replace(/{topic}/g, news.title.slice(0, 10))
  opinion = opinion.replace(/{rand}/g, Math.floor(Math.random() * 10) + 3)
  opinion = opinion.replace(/{rand2}/g, Math.floor(Math.random() * 6) + 2)
  return opinion
}

function generateDeepOpinion(role, news) {
  let roleForDeep = role
  if (!role.opinion_templates || role.is_custom) {
    const generated = generateOpinionTemplates(role.name)
    roleForDeep = {
      ...role,
      opinion_templates: generated.opinion_templates,
      speaking_style: role.speaking_style || generated.speaking_style
    }
  }
  const shortOpinion = generateOpinion(roleForDeep, news)
  return generateDeepOpinionByStyle(roleForDeep, news, shortOpinion)
}

export function getRandomPerspective() {
  const role = MOCK_ROLES[Math.floor(Math.random() * MOCK_ROLES.length)]
  let baseRarity = role.base_rarity || 'n'

  if (Math.random() < 0.15) {
    const rarityOrder = ['n', 'r', 'sr', 'ssr', 'ur']
    const idx = rarityOrder.indexOf(baseRarity)
    if (idx < rarityOrder.length - 1 && Math.random() < 0.4) {
      baseRarity = rarityOrder[idx + 1]
    }
  }

  const rarityCfg = RARITY_CONFIG[baseRarity]
  return {
    name: role.name,
    emoji: role.emoji,
    local_image: role.local_image,
    card_image: role.card_image,
    stats: role.stats,
    character_prompt: role.character_prompt,
    color: rarityCfg.color,
    description: role.description,
    keywords: role.keywords,
    interested_categories: role.interested_categories,
    opinion_templates: role.opinion_templates,
    speaking_style: role.speaking_style,
    rarity: baseRarity,
    rarity_name: rarityCfg.name,
    rarity_label: rarityCfg.label,
    glow: rarityCfg.glow,
    is_llm_generated: false
  }
}

