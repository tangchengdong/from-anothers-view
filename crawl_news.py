def strip_html(text):
    if not text:
        return ""
    return re.sub(r"<[^>]+>", "", text).strip()


def generate_id(link, title, source):
    key = link or title or source
    h = hashlib.md5(key.encode("utf-8")).hexdigest()
    return int(h[:8], 16) % 9000000 + 100000


def parse_date(entry):
    for field in ["published_parsed", "updated_parsed"]:
        t = entry.get(field)
        if t:
            try:
                return datetime(*t[:6]).strftime("%Y-%m-%d")
            except Exception:
                pass
    for field in ["published", "updated", "created"]:
        s = entry.get(field)
        if s:
            return s[:10] if len(s) >= 10 else s
    return datetime.now().strftime("%Y-%m-%d")


def extract_image(entry):
    for key in ["media_thumbnail", "media_content"]:
        media = entry.get(key)
        if media and isinstance(media, list) and media[0].get("url"):
            return media[0]["url"]
    enclosures = entry.get("enclosures", [])
    for enc in enclosures:
        if enc.get("type", "").startswith("image") and enc.get("href"):
            return enc["href"]
    content = entry.get("content", [{}])
    if isinstance(content, list) and content:
        html = content[0].get("value", "")
    else:
        html = entry.get("summary", "") or entry.get("description", "")
    match = re.search(r'<img[^>]+src=["\']([^"\']+)["\']', html)
    if match:
        return match.group(1)
    title = entry.get("title", "news")
    prompt = title[:20].replace(" ", "%20")
    return f"https://trae-api-cn.mchost.guru/api/ide/v1/text_to_image?prompt={prompt}%20news%20article&image_size=square_hd"


def classify_perspective(title, summary, tags):
    """根据关键词将资讯分类到视角"""
    text = f"{title} {summary} {' '.join(tags)}".lower()
    scores = {}
    for pid, keywords in PERSPECTIVE_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw.lower() in text)
        if score > 0:
            scores[pid] = score
    if scores:
        return max(scores, key=scores.get)
    return None


def fetch_feed(client, feed_config):
    """抓取单个 RSS 源"""
    url = feed_config["url"]
    name = feed_config["name"]
    try:
        resp = client.get(url, timeout=12.0)
        resp.raise_for_status()
        parsed = feedparser.parse(resp.content)
        items = []
        for entry in parsed.entries[:20]:
            title = entry.get("title", "").strip()
            if not title:
                continue
            summary = strip_html(entry.get("summary", "") or entry.get("description", ""))
            if len(summary) > 120:
                summary = summary[:120] + "..."
            content = strip_html(
                (entry.get("content", [{}])[0].get("value", "") if entry.get("content") else "")
                or entry.get("summary", "")
                or entry.get("description", "")
            )
            if len(content) > 2000:
                content = content[:2000] + "..."

            tags = feed_config.get("tags", [])[:3]
            # 尝试按内容分类到其他视角
            classified = classify_perspective(title, summary, tags)
            perspective_id = classified or feed_config.get("perspective", "tech")

            categories = PERSPECTIVE_CATEGORIES.get(perspective_id, ["综合资讯"])
            category = categories[0] if not classified else categories[
                min(len(tags), len(categories) - 1)
            ]

            item = {
                "id": generate_id(entry.get("link", ""), title, name),
                "title": title,
                "summary": summary or title,
                "content": content or title,
                "source": name,
                "category": category,
                "tags": tags,
                "publish_time": parse_date(entry),
                "views": hash(title) % 5000000 + 100000,
                "image_url": extract_image(entry),
                "perspective_id": perspective_id,
                "link": entry.get("link", ""),
                "is_rss": True,
            }
            items.append(item)
        print(f"  [OK] {name}: {len(items)} 条")
        return items
    except Exception as e:
        print(f"  [FAIL] {name}: {type(e).__name__}")
        return []


