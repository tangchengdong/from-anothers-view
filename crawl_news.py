def crawl_all():
    """抓取所有 RSS 源并存入本地"""
    print("=" * 60)
    print("开始爬取 RSS 源...")
    print("=" * 60)

    headers = {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 "
                       "(KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36"
    }
    all_items = []
    with httpx.Client(headers=headers, follow_redirects=True, timeout=12.0) as client:
        for feed in RSS_FEEDS:
            items = fetch_feed(client, feed)
            all_items.extend(items)

    # 去重（按 ID）
    seen_ids = set()
    unique_items = []
    for item in all_items:
        if item["id"] not in seen_ids:
            seen_ids.add(item["id"])
            unique_items.append(item)

    # 合并特色视角专题数据（lgbtq/environment/disability/rural/elderly/global）
    featured_items = generate_featured_news()
    for item in featured_items:
        if item["id"] not in seen_ids:
            seen_ids.add(item["id"])
            unique_items.append(item)
    print(f"\n特色视角专题数据: {len(featured_items)} 条")

    # 按视角统计
    print(f"\n{'=' * 60}")
    print(f"总计抓取: {len(unique_items)} 条（去重后）")
    print(f"{'=' * 60}")
    perspective_counts = {}
    for item in unique_items:
        pid = item.get("perspective_id", "other")
        perspective_counts[pid] = perspective_counts.get(pid, 0) + 1
    for pid, count in sorted(perspective_counts.items()):
        name = PERSPECTIVE_NAMES.get(pid, pid)
        print(f"  {name}({pid}): {count} 条")

    # 按发布时间排序
    unique_items.sort(key=lambda x: x.get("publish_time", ""), reverse=True)

    # 存入本地文件
    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(unique_items, f, ensure_ascii=False, indent=2)
    print(f"\n已保存到: {OUTPUT_FILE}")
    print(f"文件大小: {OUTPUT_FILE.stat().st_size / 1024:.1f} KB")

    return unique_items


if __name__ == "__main__":
    items = crawl_all()
    print(f"\n完成！共 {len(items)} 条资讯已存入本地。")
